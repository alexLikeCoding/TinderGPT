for tag, rule in rules:
    conn.execute(
        'INSERT INTO pickup_rules (tag, rule, TindeBielik_rules) VALUES (?, ?, ?)',
        (tag, rule.strip(), rule.strip())
    )

conn.commit()
conn.close()
print(f'Seeded {len(rules)} rules into {db_path}')
